-- =========================================================================
-- DANH SÁCH CÁC BẢNG LIÊN QUAN INVENTORY
-- =========================================================================
CREATE TABLE IF NOT EXISTS wms_stocks (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id BIGINT NOT NULL,
  name VARCHAR(255) NOT NULL,
  unit VARCHAR(50) NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT unique_wms_stocks_tenant_name UNIQUE (tenant_id, name)
);

CREATE TABLE IF NOT EXISTS wms_contents (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id BIGINT NOT NULL,
  stock_id BIGINT NOT NULL,
  uname VARCHAR(255) NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT unique_wms_contents_tenant_stock UNIQUE (tenant_id, stock_id)
);

CREATE TABLE IF NOT EXISTS wms_lots (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id BIGINT NOT NULL,
  lot_number VARCHAR(255) NOT NULL,
  quantity INTEGER NOT NULL DEFAULT 0,
  supplier VARCHAR(255),
  entry_date TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  cost_price DOUBLE PRECISION,
  status INTEGER DEFAULT 0,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT unique_wms_lots_tenant_lot_number UNIQUE (tenant_id, lot_number)
);

CREATE TABLE IF NOT EXISTS wms_stock_entries (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id BIGINT NOT NULL,
  stock_id BIGINT NOT NULL,
  lot_id BIGINT NOT NULL,
  quantity INTEGER NOT NULL,
  status INTEGER NOT NULL,
  expired_at TIMESTAMP,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT unique_wms_stock_entries_tenant_stock_lot UNIQUE (tenant_id, stock_id, lot_id)
);

CREATE TABLE IF NOT EXISTS wms_shelves (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id BIGINT NOT NULL,
  name VARCHAR(255) NOT NULL,
  publish BOOLEAN DEFAULT FALSE,
  description VARCHAR(255),
  node INTEGER DEFAULT 0,
  zone INTEGER DEFAULT 0,
  is_left BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT unique_wms_shelves_tenant_name UNIQUE (tenant_id, name)
);

CREATE TABLE IF NOT EXISTS wms_stock_shelves (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id BIGINT NOT NULL,
  stock_id BIGINT NOT NULL,
  shelf_id BIGINT NOT NULL,
  quantity INTEGER NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT unique_wms_stock_shelves_tenant_stock_shelf UNIQUE (tenant_id, stock_id, shelf_id)
);

-- Bảng Partition mẫu: wms_sales
CREATE TABLE IF NOT EXISTS wms_sales (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  tenant_id BIGINT NOT NULL,
  order_id BIGINT NOT NULL,
  cost_price DOUBLE PRECISION NOT NULL,
  status INTEGER DEFAULT 0,
  version INTEGER DEFAULT 0,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (id, created_at), -- Bắt buộc phải có created_at trong PK để phân vùng
  CONSTRAINT unique_wms_sales_tenant_order UNIQUE (tenant_id, order_id, created_at)
) PARTITION BY RANGE (created_at);

-- Bảng Partition mẫu: wms_sale_events
CREATE TABLE IF NOT EXISTS wms_sale_events (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  tenant_id BIGINT NOT NULL,
  sale_id BIGINT NOT NULL,
  stock_id BIGINT NOT NULL,
  count INTEGER NOT NULL,
  version INTEGER DEFAULT 0,
  status INTEGER DEFAULT 0,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (id, created_at),
  CONSTRAINT unique_wms_sale_events_tenant_sale_id UNIQUE (tenant_id, sale_id, stock_id, version, created_at)
) PARTITION BY RANGE (created_at);

CREATE TABLE IF NOT EXISTS wms_items (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id BIGINT NOT NULL,
  stock_id BIGINT NOT NULL,
  lot_id BIGINT NOT NULL,
  shelf_id BIGINT,
  order_id BIGINT,
  assigned_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  expired_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  status INTEGER DEFAULT 0,
  cost_price DOUBLE PRECISION NOT NULL,
  barcode VARCHAR(255)
);


-- =========================================================================
-- DANH SÁCH CÁC BẢNG LIÊN QUAN TOPOLOGY
-- =========================================================================

CREATE TABLE IF NOT EXISTS wms_zones (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id BIGINT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  name VARCHAR(255) NOT NULL,
  description TEXT,
  pos_x REAL NOT NULL DEFAULT 0, -- Chuyển sang REAL hoặc DOUBLE PRECISION
  pos_y REAL NOT NULL DEFAULT 0,
  height REAL NOT NULL,
  width REAL NOT NULL,
  CONSTRAINT unique_wms_zones_tenant_name UNIQUE (tenant_id, name)
);

CREATE TABLE IF NOT EXISTS wms_nodes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id BIGINT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  zone_id BIGINT,
  name VARCHAR(255),
  kind INTEGER NOT NULL DEFAULT 0,
  pos_x REAL NOT NULL DEFAULT 0,
  pos_y REAL NOT NULL DEFAULT 0,
  CONSTRAINT unique_wms_nodes_tenant_name UNIQUE (tenant_id, name)
);

CREATE TABLE IF NOT EXISTS wms_paths (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id BIGINT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  from_node_id BIGINT,
  to_node_id BIGINT,
  name VARCHAR(50),
  zone_id BIGINT,
  distance REAL,
  is_one_way BOOLEAN DEFAULT FALSE,
  sharps JSONB, -- Sử dụng JSONB tối ưu hiệu năng xử lý hình học/tọa độ phẳng
  status INTEGER NOT NULL DEFAULT 0,
  CONSTRAINT unique_wms_paths_tenant_name UNIQUE (tenant_id, name)
);


