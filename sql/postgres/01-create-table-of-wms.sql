-- Bảng Partition mẫu: wms_picking_plans
CREATE TABLE IF NOT EXISTS wms_picking_plans (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  tenant_id BIGINT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  version INTEGER DEFAULT 0,
  status INTEGER,
  PRIMARY KEY (id, created_at)
) PARTITION BY RANGE (created_at);

CREATE TABLE IF NOT EXISTS wms_picking_plans_in_zones (
  id BIGINT PRIMARY KEY,
  tenant_id BIGINT NOT NULL,
  plan_id BIGINT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS wms_picking_plans_in_nodes (
  id BIGINT PRIMARY KEY,
  tenant_id BIGINT NOT NULL,
  plan_id BIGINT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Bảng Partition mẫu: wms_picking_routes
CREATE TABLE IF NOT EXISTS wms_picking_routes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  tenant_id BIGINT NOT NULL,
  depend_id BIGINT,
  picking_id BIGINT,
  status INTEGER,
  version INTEGER DEFAULT 0,
  paths JSONB,
  PRIMARY KEY (id, created_at)
) PARTITION BY RANGE (created_at);

-- Bảng Partition mẫu: wms_picking_goods
CREATE TABLE IF NOT EXISTS wms_picking_goods (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  tenant_id BIGINT NOT NULL,
  sale_id BIGINT NOT NULL,
  plan_id BIGINT NOT NULL,
  route_id BIGINT,
  event_id BIGINT,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  status INTEGER,
  is_ready_to_pack BOOLEAN,
  PRIMARY KEY (id, created_at),
  CONSTRAINT unique_wms_picking_goods_tenant_sale UNIQUE (tenant_id, sale_id, created_at)
) PARTITION BY RANGE (created_at);

-- Bảng Partition mẫu: wms_picking_plan_events
CREATE TABLE IF NOT EXISTS wms_picking_plan_events (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  tenant_id BIGINT NOT NULL,
  plan_id BIGINT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  version INTEGER NOT NULL,
  status INTEGER NOT NULL,
  PRIMARY KEY (id, created_at),
  CONSTRAINT unique_wms_picking_plan_events_tenant_plan_status UNIQUE (tenant_id, plan_id, version, created_at)
) PARTITION BY RANGE (created_at);

-- Bảng Partition mẫu: wms_picking_route_events
CREATE TABLE IF NOT EXISTS wms_picking_route_events (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  tenant_id BIGINT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  version INTEGER NOT NULL,
  actor_id BIGINT NOT NULL,
  route_id BIGINT NOT NULL,
  status INTEGER NOT NULL,
  PRIMARY KEY (id, created_at),
  CONSTRAINT unique_wms_picking_route_events_tenant_route_status UNIQUE (tenant_id, route_id, version, created_at)
) PARTITION BY RANGE (created_at);

-- Bảng Partition mẫu: wms_picking_items
CREATE TABLE IF NOT EXISTS wms_picking_items (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  tenant_id BIGINT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  item_id BIGINT,
  event_id BIGINT,
  route_id BIGINT,
  ledger_id BIGINT,
  barcode VARCHAR(255),
  PRIMARY KEY (id, created_at),
  CONSTRAINT unique_wms_picking_items_tenant_item_event UNIQUE (tenant_id, item_id, created_at)
) PARTITION BY RANGE (created_at);


