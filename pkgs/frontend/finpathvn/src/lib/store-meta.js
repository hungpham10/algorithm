export function getStoreMeta(name) {
  const cleanName = String(name || '').trim();

  if (storeMetadata[cleanName]) {
    return storeMetadata[cleanName];
  }

  for (const key of Object.keys(storeMetadata)) {
    if (cleanName.toLowerCase().includes(storeMetadata[key].shortName.toLowerCase()) ||
        key.toLowerCase().includes(cleanName.toLowerCase())) {
      return storeMetadata[key];
    }
  }

  return {
    shortName: cleanName.split('-')[0].trim().slice(0, 25),
    website: null,
    phone: null,
    description: `Tiệm vàng ${cleanName} chuyên cung cấp các loại sản phẩm vàng bạc trang sức, vàng miếng giao dịch uy tín tại thị trường Việt Nam.`,
    badge: "Tiệm vàng uy tín",
    theme: defaultTheme
  };
}
